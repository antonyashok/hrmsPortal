package com.tm.invoice.mongo.domain;

public class Status {

    private int code;
    private String status;

    public Status() {}

    public Status(String status) {
        this.status = status;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
